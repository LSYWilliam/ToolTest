interface TableInterface {
    /**table表头数据*/
    tableHeaderData?: any;
    /**table表是否分页*/
    paginationBool?: boolean;
    /**table表每一页的大小*/
    sizeEveryPage?: number;
    /**table表的行高*/
    tableRowHeight?: number;
    /**table表格的整体样式*/
    tableStyle?: any;
    /**table表格是否可以多选*/
    tableSelection?: any;
    /** 右键菜单是否开启 */
    rightMenu?: boolean;
}
